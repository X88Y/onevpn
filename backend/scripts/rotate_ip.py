class TimeWebBalancerClient:
    def __init__(self, headers: dict, base_url: str = "https://timeweb.cloud"):
        self.headers = headers
        self.base_url = base_url.rstrip("/")

    def list_balancers(self) -> list:
        resp = httpx.get(f"{self.base_url}/api/v1/balancers", headers=self.headers, timeout=30.0)
        if resp.status_code != 200:
            raise RuntimeError(f"Failed to list balancers: {resp.status_code} {resp.text}")
        data = resp.json()
        return data.get("balancers") or []

    def find_balancer_by_name(self, token_idx: int) -> Optional[dict]:
        name = _balancer_name(token_idx)
        for balancer in self.list_balancers():
            if balancer.get("name") == name:
                return balancer
        return None

    def find_token_balancer(
        self,
        token_idx: int,
        wl_ip: Optional[str] = None,
        zone: Optional[str] = None,
    ) -> Optional[dict]:
        balancer = self.find_balancer_by_name(token_idx)
        if balancer is not None:
            if zone is None or balancer.get("availability_zone") == zone:
                return balancer
            return None

        if wl_ip:
            for balancer in self.list_balancers():
                if balancer.get("ip") != wl_ip:
                    continue
                if zone is None or balancer.get("availability_zone") == zone:
                    return balancer
        return None

    def ensure_balancer_for_zone(
        self,
        token_idx: int,
        zone: str,
        wl_ip: Optional[str] = None,
    ) -> Optional[dict]:
        balancer = self.find_balancer_by_name(token_idx)
        if balancer is not None and balancer.get("availability_zone") != zone:
            print(
                f"  Deleting balancer {balancer['id']} in zone "
                f"{balancer.get('availability_zone')} (need {zone})..."
            )
            self.delete_balancer(int(balancer["id"]))
            balancer = None
        if balancer is not None:
            return balancer
        return self.find_token_balancer(token_idx, wl_ip, zone)

