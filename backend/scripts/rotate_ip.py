    if ip_item.get("resource_id") is not None:
        return True
    if ip_item.get("server_id") is not None:
        return True
    resource = ip_item.get("resource")
    if resource:
        if isinstance(resource, dict):
            r_id = resource.get("id") or resource.get("resource_id")
            if r_id is not None:
                return True
        else:
            return True
    return False

