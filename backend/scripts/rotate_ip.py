def load_cidr_whitelist(file_path):
    print(f"Loading CIDR whitelist from {file_path}...")
    networks = []
    with open(file_path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            try:
                networks.append(ipaddress.ip_network(line))
            except ValueError as e:
                print(f"Warning: skipping invalid CIDR line '{line}': {e}")
    print(f"Loaded {len(networks)} CIDR networks.")
    return networks

def is_ip_in_whitelist(ip_str, networks):
    try:
        ip = ipaddress.ip_address(ip_str)
        for net in networks:
            if ip in net:
                return True
    except ValueError:
        print(f"Warning: invalid IP address received: {ip_str}")
    return False

