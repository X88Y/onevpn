    def resolve_balancer_preset(self, zone: str) -> int:
        if zone in RU_BALANCER_ZONES:
            return BALANCER_PRESET_ID
        resp = httpx.get(f"{self.base_url}/api/v1/presets/balancers", headers=self.headers, timeout=30.0)
        if resp.status_code != 200:
            return BALANCER_PRESET_ID
        presets = resp.json().get("balancers_presets") or []
        zone_prefix = zone.rsplit("-", 1)[0]
        for preset in presets:
            location = str(preset.get("location") or "")
            if location == zone or location == zone_prefix or zone.startswith(location):
                if preset.get("replica_count", 1) == 1:
                    return int(preset["id"])
        for preset in presets:
            location = str(preset.get("location") or "")
            if location == zone or location == zone_prefix:
                return int(preset["id"])
        return BALANCER_PRESET_ID

    def get_default_project_id(self) -> int:
        resp = httpx.get(f"{self.base_url}/api/v1/projects", headers=self.headers, timeout=30.0)
        if resp.status_code != 200:
            raise RuntimeError(f"Failed to list projects: {resp.status_code} {resp.text}")
        projects = resp.json().get("projects") or []
        if not projects:
            raise RuntimeError("No Timeweb projects found")
        for project in projects:
            if project.get("is_default"):
                return int(project["id"])
        return int(projects[0]["id"])

