def clean_unused_ips(headers, base_url, networks, token_idx):
    print(f"Token {token_idx}: Fetching all floating IPs to clean up unused ones...")
    try:
        resp = httpx.get(f"{base_url}/api/v1/floating-ips", headers=headers, timeout=30.0)
        if resp.status_code != 200:
            print(f"Warning: Failed to fetch floating IPs for cleanup: {resp.status_code} {resp.text}")
            return
        
        data = resp.json()
        ips_list = data.get("floating_ips") or data.get("ips") or []
        deleted_count = 0
        for ip_item in ips_list:
            ip_str = ip_item.get("ip")
            ip_id = ip_item.get("id")
            if not ip_str or not ip_id:
                continue
            
            connected = is_ip_connected(ip_item)
            whitelisted = is_ip_in_whitelist(ip_str, networks)
            
            if not connected and not whitelisted:
                print(f"Token {token_idx}: IP {ip_str} (ID: {ip_id}) is unused and NOT whitelisted. Deleting...")
                try:
                    del_resp = httpx.delete(f"{base_url}/api/v1/floating-ips/{ip_id}", headers=headers, timeout=30.0)
                    if del_resp.status_code not in (200, 204):
                        print(f"Warning: Failed to delete IP {ip_str} (status code {del_resp.status_code}): {del_resp.text}")
                    else:
                        print(f"Token {token_idx}: IP {ip_str} deleted successfully.")
                        deleted_count += 1
                except Exception as e:
                    print(f"Warning: Error occurred during IP deletion: {e}")
            else:
                reason = []
                if connected:
                    reason.append("connected to a service")
                if whitelisted:
                    reason.append("whitelisted")
                print(f"Token {token_idx}: Keeping IP {ip_str} ({' and '.join(reason)}).")
                if not connected and whitelisted:
                    setup_ws67_balancer_and_host(
                        headers,
                        token_idx,
                        ip_id,
                        ip_str,
                        availability_zone=_floating_ip_zone(ip_item),
                    )
        print(f"Token {token_idx}: Cleanup finished. Deleted {deleted_count} unused and non-whitelisted IPs.")
    except Exception as e:
        print(f"Warning: Error during floating IPs cleanup: {e}")

def rotate_ip_for_token(token, networks, token_idx):
    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json",
        "Accept": "application/json"
    }
    base_url = "https://timeweb.cloud"
    
    # Verify token by getting projects
    try:
        resp = httpx.get(f"{base_url}/api/v1/projects", headers=headers, timeout=30.0)
        if resp.status_code != 200:
            if resp.status_code == 403:
                print(f"Token {token_idx} is forbidden (status code 403). Cleaning up and skipping.")
                clean_unused_ips(headers, base_url, networks, token_idx)
                return
            print(f"Error: Token {token_idx} is invalid (status code {resp.status_code}): {resp.text}")
            sys.exit(1)
        print(f"Token {token_idx}: Connected successfully to Timeweb Cloud.")
    except Exception as e:
        print(f"Error: Connection failed for Token {token_idx}: {e}")
        sys.exit(1)

    zones = ["spb-1", "spb-2", "spb-3", "spb-4", "nsk-1", "msk-1", "msk-1", "msk-1", "msk-1", "msk-1", "msk-1"]
    attempt = 0
    while True:
        attempt += 1
        zone = random.choice(zones)
        print(f"Token {token_idx} (Attempt {attempt}): Creating floating IP in {zone}...")
        
        # 1. Create floating IP
        payload = {
            "availability_zone": zone,
            "comment": f"mvm-rotator-ip-{token_idx}",
            "is_ddos_guard": False
        }
        
        try:
            resp = httpx.post(f"{base_url}/api/v1/floating-ips", headers=headers, json=payload, timeout=30.0)
            if resp.status_code != 201:
                if resp.status_code == 403:
                    print(f"Token {token_idx} returned 403 (daily limit exceeded or forbidden). Cleaning up and skipping.")
                    clean_unused_ips(headers, base_url, networks, token_idx)
                    return
                print(f"Error creating floating IP (status code {resp.status_code}): {resp.text}")
                sys.exit(1)
            
            data = resp.json()
            floating_ip = data.get("floating_ip") or data.get("ip") or {}
            ip_str = floating_ip.get("ip")
            ip_id = floating_ip.get("id")
            ip_zone = floating_ip.get("availability_zone") or zone
            
            if not ip_str or not ip_id:
                print(f"Error: Invalid API response format when creating floating IP: {data}")
                sys.exit(1)
                
            print(f"Token {token_idx} (Attempt {attempt}): Created IP {ip_str} (ID: {ip_id})")
        except Exception as e:
            print(f"Error occurred during IP creation: {e}")
            sys.exit(1)
            
        # 2. Check if IP is whitelisted
        whitelisted = is_ip_in_whitelist(ip_str, networks)
        if whitelisted:
            print(f"Token {token_idx} (Attempt {attempt}): Success! IP {ip_str} is in the whitelist. Keeping it.")
            setup_ws442_balancer_and_host(
                headers,
                token_idx,
                ip_id,
                ip_str,
                availability_zone=zone,
            )
            return ip_str
            
        # 3. If not whitelisted, delete it and try again
        print(f"Token {token_idx} (Attempt {attempt}): IP {ip_str} is NOT in the whitelist. Deleting...")
        try:
            del_resp = httpx.delete(f"{base_url}/api/v1/floating-ips/{ip_id}", headers=headers, timeout=30.0)
            if del_resp.status_code not in (200, 204):
                print(f"Error deleting floating IP {ip_str} (status code {del_resp.status_code}): {del_resp.text}")
                sys.exit(1)
            print(f"Token {token_idx} (Attempt {attempt}): IP {ip_str} deleted successfully.")
        except Exception as e:
            print(f"Error occurred during IP deletion: {e}")
            sys.exit(1)
            
        # Wait a short duration between rotations to avoid overloading the API
        time.sleep(1.0)

